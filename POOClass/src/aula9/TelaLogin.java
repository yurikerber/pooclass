    private void login() {
        if (validar()) {
            dispose();
            TelaSistema tela = new TelaSistema();
            tela.setVisible(true);
        } else {
            Globais.exibirMensagem("Usuário ou Senha incorretos!");
            txtSenha.setText("");
        }
    }

    private boolean validar() {
        for (Usuario objUser : arrayUsuario) {
            if (txtUsuario.getText().equals(objUser.getUsuario()) && Globais.gerarMD5(txtSenha.getText()).equals(objUser.getSenha())) {
                return true;
            }
        }
        return false;
    }
