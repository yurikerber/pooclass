    public TelaLogin() {
        initComponents();

        if (objArquivo.abrirLeitura()) {
            String linha;
            String vetor[];
            do {
                linha = objArquivo.lerLinha();
                if ((linha != null)) {
                    vetor = linha.split(";");
                    objUsuario = new Usuario(vetor[0],vetor[1]);
                    arrayUsuario.add(objUsuario);
                }
            } while (linha != null);
        } else {
           Globais.exibirMensagem("Erro ao carregar arquivo!");
        }

        objArquivo.fecharArquivo();

    }

