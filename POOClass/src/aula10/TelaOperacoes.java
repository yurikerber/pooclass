    public void atualizarLbls() {
        lblAgencia.setText(objConta.getAgencia());
        lblConta.setText(objConta.getConta());
        lblTitular.setText(objConta.getTitular());
        lblSaldo.setText(String.valueOf("R$ " + objConta.getSaldo()));
        lblChegueEspecial.setText(String.valueOf("R$ " + objConta.getChequeEspecial()));
        lblChavePix.setText(objConta.getChavePix());
    }

