    private void btnRealizarOperacaoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRealizarOperacaoActionPerformed

        if (rdbDeposito.isSelected()) {
            objConta.deposito(Double.parseDouble(txtValor.getText()), false);
            atualizarLbls();
            grpOperacoes.clearSelection();
            Globais.exibirMensagem("Sucesso!");

        } else if (rdbSaque.isSelected()) {
            objConta.saque(Double.parseDouble(txtValor.getText()), false);
            atualizarLbls();
            grpOperacoes.clearSelection();
            Globais.exibirMensagem("Sucesso!");

        } else if (rdbTranferencia.isSelected()) {
            objContaDestino = (ContaBanco) jcmbContaDestino.getSelectedItem();
            objConta.transferir(Double.parseDouble(txtValor.getText()), objContaDestino);
            jcmbContaDestino.setVisible(false);
            grpOperacoes.clearSelection();
            atualizarLbls();
            atualizarLblsDestino();

        }

    }//GEN-LAST:event_btnRealizarOperacaoActionPerformed

