    public TelaLogin() {
        initComponents();
        try {

            if (objArquivo.abrirLeitura()) {
                String linha;
                String vetor[];

                String agencia;
                String conta;
                String titular;
                double saldo;
                double chequeEspecial;
                String chavePix;
                String senha;

                do {
                    linha = objArquivo.lerLinha();
                    if (linha != null && !linha.equals("")) {
                        vetor = linha.split(";");

                        agencia = vetor[0];
                        conta = vetor[1];
                        titular = vetor[2];
                        saldo = Double.parseDouble(vetor[3]);
                        chequeEspecial = Double.parseDouble(vetor[4]);
                        chavePix = vetor[5];
                        senha = vetor[6];
                        objConta = new ContaBanco(agencia, conta, titular, saldo, chequeEspecial, chavePix, senha);
                        arrayContas.add(objConta);
                    }
                } while (linha != null);

            } else {
                Globais.exibirMensagem("Erro ao carregar arquivo!");
            }

            objArquivo.fecharArquivo();
        } catch (NumberFormatException ex) {
            Globais.exibirMensagem("Erro de conversão: " + ex.getMessage());
        } catch (Exception e) {
            Globais.exibirMensagem("Erro: " + e.getMessage());
        }
    }

