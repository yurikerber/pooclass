    private void login() {
        if (validar()) {
            TelaMenu.objContaBanco = objConta;
            TelaMenu.arrayContas = arrayContas;
            
            TelaMenu tela = new TelaMenu();
            tela.setVisible(true);
            dispose(); //fechar a tela atual
        } else {
            Globais.exibirMensagem("Usuário ou Senha incorretos!");
            txtSenha.setText("");
        }
    }

    private boolean validar() {
        for (ContaBanco objUser : arrayContas) {
            if (txtConta.getText().equals(objUser.getConta()) && Globais.gerarMD5(txtSenha.getText()).equals(objUser.getSenha())) {
                objConta = objUser;
                return true;
            }
        }
        return false;
    }

